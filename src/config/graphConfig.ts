export function getEdgeStylePreset(type: string, lineWidth = 1) {
  const edgeGap = 6
  const base = {
    lineWidth: Math.max(1, lineWidth),
    startArrow: false,
    endArrow: false,
    startArrowSize: 8,
    endArrowSize: 8,
    smartRouting: true,
  }
  if (type === 'dashed-line') return { ...base, lineDash: [8, 4] }
  if (type === 'dotted-line') return { ...base, lineDash: [2, 4] }
  if (type === 'thick-line') return { ...base, lineWidth: Math.max(lineWidth, 5) }
  if (type === 'double-line' || type === 'double-polyline') {
    return {
      ...base,
      doubleLineGap: edgeGap,
      lineDash: [],
      startArrowSize: 16,
      endArrowSize: 16,
      startArrowLineWidth: Math.max(1.2, lineWidth),
      endArrowLineWidth: Math.max(1.2, lineWidth),
    }
  }
  return { ...base }
}

